def read_idx(image):
	# _____________________________________________
	# _________________ VARIABLES _________________ 
	file1 = open('index.txt', 'r')
	image = image.getT()
	volu  = np.array([])
	# _____________________________________________
	# _______________READ INDEX FILE_______________ 
	while True:
		line = file1.readline()
		if not line:
			break
		elmt = image[int(line)]
		volu = np.append(volu, elmt)
	file1.close()

	return volu

